"""
Cross-checks prompt.csv (raw model answers) against eval.csv (judge scores) to
verify the two files come from the same pipeline run and are safe to use
together as portfolio evidence.

Why this exists: while consolidating evidence for Decision Log 5, a manual
spot-check found that some o4-mini rows in prompt.csv contain
"ERROR: Connection error." / "ERROR: empty response returned", yet eval.csv
scores those same question_ids with substantive judge reasoning that clearly
describes a real answer. That can only happen if eval.csv was generated
against an earlier, different prompt.csv. This script makes that check
systematic instead of relying on a manual spot-check.

Run from the project root: python -m pipeline.check_evidence
"""
import csv
from collections import defaultdict

ERROR_PREFIXES = ("ERROR:",)
EMPTY_MARKERS = ("", "nan")


def load_csv(filepath: str) -> list[dict]:
    with open(filepath, encoding="utf-8") as f:
        return list(csv.DictReader(f))


def is_failed_answer(text: str) -> bool:
    t = (text or "").strip()
    return t.lower() in EMPTY_MARKERS or t.startswith(ERROR_PREFIXES)


def cross_check(prompt_rows: list[dict], eval_rows: list[dict]):
    """
    For every (question_id, model) pair, compares whether prompt.csv shows a
    failed/empty answer against whether eval.csv treated it as scoreable.

    Returns a list of mismatch dicts describing the inconsistency found.
    """
    eval_index = {(r["question_id"], r["model"]): r for r in eval_rows}
    mismatches = []

    for p in prompt_rows:
        key = (p["question_id"], p["model"])
        e = eval_index.get(key)
        if e is None:
            mismatches.append({
                "question_id": key[0], "model": key[1],
                "issue": "missing_in_eval",
                "detail": "Row exists in prompt.csv but has no matching row in eval.csv",
            })
            continue

        prompt_failed = is_failed_answer(p["model_answer"])
        eval_score = e["llm_score"]
        eval_treated_as_skip = eval_score == "SKIP"
        eval_treated_as_scored = eval_score in ("0", "1", "2", "3")

        if prompt_failed and eval_treated_as_scored:
            mismatches.append({
                "question_id": key[0], "model": key[1],
                "issue": "stale_eval_score",
                "detail": (
                    f"prompt.csv shows a failed answer ({p['model_answer'][:40]!r}) "
                    f"but eval.csv scored it {eval_score} with reasoning that does not "
                    f"match a failed answer — eval.csv was likely generated from a "
                    f"different (earlier, successful) run of answer.py"
                ),
            })
        elif (not prompt_failed) and eval_treated_as_skip:
            mismatches.append({
                "question_id": key[0], "model": key[1],
                "issue": "unexpected_skip",
                "detail": (
                    f"prompt.csv shows a real answer ({len(p['model_answer'])} chars) "
                    f"but eval.csv marked it SKIP — evaluate.py may have been run "
                    f"against a different (failed) version of this row"
                ),
            })

    return mismatches


def print_report(mismatches: list[dict]):
    print("=" * 60)
    print("EVIDENCE CONSISTENCY CHECK — prompt.csv vs eval.csv")
    print("=" * 60)

    if not mismatches:
        print("\nNo mismatches found — both files come from the same run.")
        print("=" * 60)
        return

    by_model = defaultdict(int)
    for m in mismatches:
        by_model[m["model"]] += 1

    print(f"\n{len(mismatches)} mismatch(es) found:\n")
    for m in mismatches:
        print(f"  [{m['issue']}] {m['question_id']} / {m['model']}")
        print(f"      {m['detail']}")

    print("\nMismatches per model:")
    for model, count in by_model.items():
        print(f"  {model:20s} {count}")

    print("\nConclusion: prompt.csv and eval.csv were NOT produced by the same")
    print("pipeline run. eval.csv (90/90 scored, no SKIP rows) is treated as the")
    print("canonical evidence set for the leaderboard; prompt.csv is flagged as")
    print("stale and should be regenerated by re-running pipeline.answer before")
    print("any future re-evaluation. See Decision Log 5 for the full reasoning.")
    print("=" * 60)


if __name__ == "__main__":
    prompt_rows = load_csv("prompt.csv")
    eval_rows = load_csv("eval.csv")
    print(f"Loaded {len(prompt_rows)} prompt rows and {len(eval_rows)} eval rows\n")
    mismatches = cross_check(prompt_rows, eval_rows)
    print_report(mismatches)
